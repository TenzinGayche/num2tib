def int2tib(num):
    """_summary_
    this function convert integers to Tibetan text with place value
    Args:
        num (int): number to be converted to Tibetan
    """
    decimal = len(num)
    if(decimal==1):
        return dict[num]
    arr=[]
    for i in range(1,decimal):
        if i == 1:
            if(num[1]=='0' and num[0]=='0'):
                arr
            else:
                arr.append(dict[str((int(num[1]+num[0])))]) 
